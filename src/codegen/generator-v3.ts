/**
 * 基于 Binary Schema 的 TypeScript 代码生成器
 * 
 * 使用官方 capnp 编译器生成的二进制 schema 来生成 TypeScript 代码
 */

import type { 
  CodeGeneratorRequestReader, 
  NodeReader, 
  FieldReader, 
  TypeReader,
  TypeKind,
  Id 
} from '../schema/schema-reader.js';

// 导入运行时类型用于代码生成
import type { StructReader, StructBuilder } from '../core/index.js';

export interface GeneratorOptions {
  /** 运行时库导入路径 */
  runtimeImportPath?: string;
}

const DEFAULT_OPTIONS: GeneratorOptions = {
  runtimeImportPath: '@naeemo/capnp',
};

/**
 * Union 字段分组
 */
interface UnionGroup {
  discriminantOffset: number;
  fields: FieldReader[];
}

/**
 * 从 CodeGeneratorRequest 生成 TypeScript 代码
 */
export function generateFromRequest(
  request: CodeGeneratorRequestReader,
  options?: GeneratorOptions
): Map<string, string> {
  const opts = { ...DEFAULT_OPTIONS, ...options };
  const files = new Map<string, string>();
  
  const nodes = request.nodes;
  const requestedFiles = request.requestedFiles;
  
  // 为每个请求的文件生成代码
  for (const file of requestedFiles) {
    const filename = file.filename.replace(/\.capnp$/, '.ts');
    const code = generateFile(file.id, nodes, opts);
    files.set(filename, code);
  }
  
  return files;
}

/**
 * 生成单个文件的 TypeScript 代码
 */
function generateFile(
  fileId: Id,
  allNodes: NodeReader[],
  options: GeneratorOptions
): string {
  const lines: string[] = [];
  
  // 文件头
  lines.push('// Generated by @naeemo/capnp');
  lines.push('// DO NOT EDIT MANUALLY');
  lines.push('');
  lines.push(`import { MessageReader, MessageBuilder, StructReader, StructBuilder, createUnionReader, createUnionBuilder } from "${options.runtimeImportPath}";`);
  lines.push('');
  
  // 添加 XOR 辅助函数（用于 float 默认值）
  lines.push('// XOR helpers for default value encoding');
  lines.push('function xorFloat32(a: number, b: number): number {');
  lines.push('  const view = new DataView(new ArrayBuffer(4));');
  lines.push('  view.setFloat32(0, a, true);');
  lines.push('  const aBits = view.getUint32(0, true);');
  lines.push('  view.setFloat32(0, b, true);');
  lines.push('  const bBits = view.getUint32(0, true);');
  lines.push('  view.setUint32(0, aBits ^ bBits, true);');
  lines.push('  return view.getFloat32(0, true);');
  lines.push('}');
  lines.push('');
  lines.push('function xorFloat64(a: number, b: number): number {');
  lines.push('  const view = new DataView(new ArrayBuffer(8));');
  lines.push('  view.setFloat64(0, a, true);');
  lines.push('  const aBits = view.getBigUint64(0, true);');
  lines.push('  view.setFloat64(0, b, true);');
  lines.push('  const bBits = view.getBigUint64(0, true);');
  lines.push('  view.setBigUint64(0, aBits ^ bBits, true);');
  lines.push('  return view.getFloat64(0, true);');
  lines.push('}');
  lines.push('');
  
  // 找到属于这个文件的所有节点
  const fileNodes = allNodes.filter(n => n.scopeId === fileId);
  
  // 生成每个节点的代码
  for (const node of fileNodes) {
    if (node.isStruct) {
      lines.push(generateStruct(node, allNodes));
    } else if (node.isEnum) {
      lines.push(generateEnum(node));
    }
    lines.push('');
  }
  
  return lines.join('\n');
}

/**
 * 生成 Struct 的 TypeScript 代码
 */
function generateStruct(node: NodeReader, allNodes: NodeReader[]): string {
  const lines: string[] = [];
  const structName = getShortName(node.displayName);
  
  // 分析字段：分离 Union 字段、普通字段和 Group 字段
  const { unionGroups, regularFields, groupFields } = analyzeFields(node, allNodes);
  
  // 生成接口定义
  lines.push(`export interface ${structName} {`);
  
  // 普通字段
  for (const field of regularFields) {
    if (!field.isSlot) continue;
    const tsType = getTypeScriptType(field.slotType, allNodes);
    lines.push(`  ${field.name}: ${tsType};`);
  }
  
  // Group 字段 - 内联 Group 的字段
  for (const { field: groupField, groupNode } of groupFields) {
    const groupName = groupField.name;
    for (const field of groupNode.structFields) {
      if (!field.isSlot) continue;
      const tsType = getTypeScriptType(field.slotType, allNodes);
      lines.push(`  ${groupName}${capitalize(field.name)}: ${tsType};`);
    }
  }
  
  // Union 字段 - 生成 discriminant 和 variant 类型
  for (const [groupIndex, group] of unionGroups.entries()) {
    const unionName = group.fields.length > 0 ? `${capitalize(group.fields[0].name)}Union` : `Union${groupIndex}`;
    lines.push(`  ${unionName}: ${generateUnionVariantType(group, allNodes)};`);
  }
  
  lines.push('}');
  lines.push('');
  
  // 生成 Reader 类
  lines.push(`export class ${structName}Reader {`);
  lines.push(`  constructor(private reader: StructReader) {}`);
  lines.push('');
  
  // 普通字段 getters
  for (const field of regularFields) {
    if (!field.isSlot) continue;
    const getter = generateFieldGetter(field);
    lines.push(`  ${getter}`);
    lines.push('');
  }
  
  // Group 字段 getters - 内联 Group 的字段
  for (const { field: groupField, groupNode } of groupFields) {
    const groupName = groupField.name;
    for (const field of groupNode.structFields) {
      if (!field.isSlot) continue;
      const getter = generateGroupFieldGetter(field, groupName);
      lines.push(`  ${getter}`);
      lines.push('');
    }
  }
  
  // Union 方法
  for (const [groupIndex, group] of unionGroups.entries()) {
    const unionName = group.fields.length > 0 ? `${capitalize(group.fields[0].name)}Union` : `Union${groupIndex}`;
    const variants: Record<number, string> = {};
    for (const field of group.fields) {
      variants[field.discriminantValue] = field.name;
    }
    const variantsStr = JSON.stringify(variants).replace(/"/g, "'");
    
    lines.push(`  get${unionName}Tag(): number {`);
    lines.push(`    return this.reader.getUint16(${group.discriminantOffset});`);
    lines.push(`  }`);
    lines.push('');
    
    lines.push(`  get${unionName}Variant(): string | undefined {`);
    lines.push(`    const tag = this.get${unionName}Tag();`);
    lines.push(`    const variants = ${variantsStr};`);
    lines.push(`    return variants[tag];`);
    lines.push(`  }`);
    lines.push('');
    
    // 每个 variant 的 getter
    for (const field of group.fields) {
      const getter = generateUnionFieldGetter(field, unionName, field.discriminantValue);
      lines.push(`  ${getter}`);
      lines.push('');
    }
  }
  
  lines.push('}');
  lines.push('');
  
  // 生成 Builder 类
  lines.push(`export class ${structName}Builder {`);
  lines.push(`  constructor(private builder: StructBuilder) {}`);
  lines.push('');
  
  // 普通字段 setters
  for (const field of regularFields) {
    if (!field.isSlot) continue;
    const setter = generateFieldSetter(field);
    lines.push(`  ${setter}`);
    lines.push('');
  }
  
  // Group 字段 setters
  for (const { field: groupField, groupNode } of groupFields) {
    const groupName = groupField.name;
    for (const field of groupNode.structFields) {
      if (!field.isSlot) continue;
      const setter = generateGroupFieldSetter(field, groupName);
      lines.push(`  ${setter}`);
      lines.push('');
    }
  }
  
  // Union setters
  for (const [groupIndex, group] of unionGroups.entries()) {
    const unionName = group.fields.length > 0 ? `${capitalize(group.fields[0].name)}Union` : `Union${groupIndex}`;
    for (const field of group.fields) {
      const setter = generateUnionFieldSetter(field, unionName, field.discriminantValue);
      lines.push(`  ${setter}`);
      lines.push('');
    }
  }
  
  lines.push('}');
  
  return lines.join('\n');
}

/**
 * 分析字段，分离 Union 字段和普通字段，展开 Group 字段
 */
function analyzeFields(node: NodeReader, allNodes: NodeReader[]): { 
  unionGroups: UnionGroup[], 
  regularFields: FieldReader[],
  groupFields: { field: FieldReader, groupNode: NodeReader }[] 
} {
  const fields = node.structFields;
  const unionGroups: Map<number, UnionGroup> = new Map();
  const regularFields: FieldReader[] = [];
  const groupFields: { field: FieldReader, groupNode: NodeReader }[] = [];
  
  for (const field of fields) {
    // 处理 Group 字段
    if (field.isGroup) {
      const groupNode = allNodes.find(n => n.id === field.groupTypeId);
      if (groupNode?.isStruct) {
        groupFields.push({ field, groupNode });
      }
      continue;
    }
    
    // 处理 Union 字段
    if (node.structDiscriminantCount > 0 && field.discriminantValue !== 65535 && field.discriminantValue !== 0) {
      const discriminantOffset = node.structDiscriminantOffset;
      
      if (!unionGroups.has(discriminantOffset)) {
        unionGroups.set(discriminantOffset, { discriminantOffset, fields: [] });
      }
      unionGroups.get(discriminantOffset)!.fields.push(field);
    } else {
      // 普通字段
      regularFields.push(field);
    }
  }
  
  return { 
    unionGroups: Array.from(unionGroups.values()), 
    regularFields,
    groupFields 
  };
}

/**
 * 生成 Union variant 类型
 */
function generateUnionVariantType(group: UnionGroup, allNodes: NodeReader[]): string {
  const variants = group.fields.map(f => {
    const tsType = f.isSlot ? getTypeScriptType(f.slotType, allNodes) : 'unknown';
    return `{ tag: ${f.discriminantValue}; variant: '${f.name}'; value: ${tsType} }`;
  });
  return variants.join(' | ');
}

/**
 * 生成 Union 字段的 getter
 */
function generateUnionFieldGetter(field: FieldReader, unionName: string, discriminantValue: number): string {
  const name = field.name;
  const type = field.slotType;
  
  if (!type || !field.isSlot) {
    return `get${capitalize(name)}(): unknown | undefined { return this.get${unionName}Tag() === ${discriminantValue} ? undefined : undefined; }`;
  }
  
  const returnType = getTypeScriptTypeForSetter(type);
  
  let body: string;
  switch (type.kind) {
    case 'void':
      body = `return undefined;`;
      break;
    case 'bool':
      body = `return this.reader.getBool(${field.slotOffset * 8});`;
      break;
    case 'int8':
      body = `return this.reader.getInt8(${field.slotOffset});`;
      break;
    case 'int16':
      body = `return this.reader.getInt16(${field.slotOffset * 2});`;
      break;
    case 'int32':
      body = `return this.reader.getInt32(${field.slotOffset * 4});`;
      break;
    case 'int64':
      body = `return this.reader.getInt64(${field.slotOffset * 8});`;
      break;
    case 'uint8':
      body = `return this.reader.getUint8(${field.slotOffset});`;
      break;
    case 'uint16':
      body = `return this.reader.getUint16(${field.slotOffset * 2});`;
      break;
    case 'uint32':
      body = `return this.reader.getUint32(${field.slotOffset * 4});`;
      break;
    case 'uint64':
      body = `return this.reader.getUint64(${field.slotOffset * 8});`;
      break;
    case 'float32':
      body = `return this.reader.getFloat32(${field.slotOffset * 4});`;
      break;
    case 'float64':
      body = `return this.reader.getFloat64(${field.slotOffset * 8});`;
      break;
    case 'text':
      body = `return this.reader.getText(${field.slotOffset});`;
      break;
    case 'data':
      body = `return this.reader.getData(${field.slotOffset});`;
      break;
    default:
      body = `return undefined;`;
  }
  
  return `get${capitalize(name)}(): ${returnType} | undefined {
    if (this.get${unionName}Tag() !== ${discriminantValue}) return undefined;
    ${body}
  }`;
}

/**
 * 生成 Group 字段的 getter
 * Group 字段在父 struct 的 data/pointer section 中，但命名空间是 Group 的名字
 */
function generateGroupFieldGetter(field: FieldReader, groupName: string): string {
  const name = field.name;
  const type = field.slotType;
  
  if (!type) {
    return `get${capitalize(groupName)}${capitalize(name)}(): unknown { return undefined; }`;
  }
  
  switch (type.kind) {
    case 'void':
      return `get${capitalize(groupName)}${capitalize(name)}(): void { return undefined; }`;
    case 'bool':
      return `get${capitalize(groupName)}${capitalize(name)}(): boolean { return this.reader.getBool(${field.slotOffset * 8}); }`;
    case 'int8':
      return `get${capitalize(groupName)}${capitalize(name)}(): number { return this.reader.getInt8(${field.slotOffset}); }`;
    case 'int16':
      return `get${capitalize(groupName)}${capitalize(name)}(): number { return this.reader.getInt16(${field.slotOffset * 2}); }`;
    case 'int32':
      return `get${capitalize(groupName)}${capitalize(name)}(): number { return this.reader.getInt32(${field.slotOffset * 4}); }`;
    case 'int64':
      return `get${capitalize(groupName)}${capitalize(name)}(): bigint { return this.reader.getInt64(${field.slotOffset * 8}); }`;
    case 'uint8':
      return `get${capitalize(groupName)}${capitalize(name)}(): number { return this.reader.getUint8(${field.slotOffset}); }`;
    case 'uint16':
      return `get${capitalize(groupName)}${capitalize(name)}(): number { return this.reader.getUint16(${field.slotOffset * 2}); }`;
    case 'uint32':
      return `get${capitalize(groupName)}${capitalize(name)}(): number { return this.reader.getUint32(${field.slotOffset * 4}); }`;
    case 'uint64':
      return `get${capitalize(groupName)}${capitalize(name)}(): bigint { return this.reader.getUint64(${field.slotOffset * 8}); }`;
    case 'float32':
      return `get${capitalize(groupName)}${capitalize(name)}(): number { return this.reader.getFloat32(${field.slotOffset * 4}); }`;
    case 'float64':
      return `get${capitalize(groupName)}${capitalize(name)}(): number { return this.reader.getFloat64(${field.slotOffset * 8}); }`;
    case 'text':
      return `get${capitalize(groupName)}${capitalize(name)}(): string { return this.reader.getText(${field.slotOffset}); }`;
    case 'data':
      return `get${capitalize(groupName)}${capitalize(name)}(): Uint8Array { return this.reader.getData(${field.slotOffset}); }`;
    default:
      return `get${capitalize(groupName)}${capitalize(name)}(): unknown { return undefined; }`;
  }
}


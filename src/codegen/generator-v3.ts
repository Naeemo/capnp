/**
 * 从 ValueReader 提取默认值，用于代码生成
 */
function extractDefaultValue(field: FieldReader): string | undefined {
  const defaultValue = field.defaultValue;
  if (!defaultValue) return undefined;
  
  const type = field.slotType;
  if (!type) return undefined;
  
  switch (type.kind) {
    case 'bool':
      if (defaultValue.isBool) return defaultValue.boolValue ? 'true' : 'false';
      return undefined;
    case 'int8':
      if (defaultValue.isInt8) return `${defaultValue.int8Value}`;
      return undefined;
    case 'int16':
      if (defaultValue.isInt16) return `${defaultValue.int16Value}`;
      return undefined;
    case 'int32':
      if (defaultValue.isInt32) return `${defaultValue.int32Value}`;
      return undefined;
    case 'int64':
      if (defaultValue.isInt64) return `${defaultValue.int64Value}n`;
      return undefined;
    case 'uint8':
      if (defaultValue.isUint8) return `${defaultValue.uint8Value}`;
      return undefined;
    case 'uint16':
      if (defaultValue.isUint16) return `${defaultValue.uint16Value}`;
      return undefined;
    case 'uint32':
      if (defaultValue.isUint32) return `${defaultValue.uint32Value}`;
      return undefined;
    case 'uint64':
      if (defaultValue.isUint64) return `${defaultValue.uint64Value}n`;
      return undefined;
    case 'float32':
      if (defaultValue.isFloat32) {
        const val = defaultValue.float32Value;
        if (Number.isNaN(val)) return 'NaN';
        if (val === Infinity) return 'Infinity';
        if (val === -Infinity) return '-Infinity';
        return `${val}`;
      }
      return undefined;
    case 'float64':
      if (defaultValue.isFloat64) {
        const val = defaultValue.float64Value;
        if (Number.isNaN(val)) return 'NaN';
        if (val === Infinity) return 'Infinity';
        if (val === -Infinity) return '-Infinity';
        return `${val}`;
      }
      return undefined;
    case 'enum':
      if (defaultValue.isEnum) return `${defaultValue.enumValue}`;
      return undefined;
    default:
      return undefined;
  }
}

/**
 * 生成 XOR 解码表达式（用于 getter）
 * stored ^ default = actual
 */
function generateXorDecode(expr: string, defaultValue: string, typeKind: TypeKind): string {
  switch (typeKind) {
    case 'bool':
      // bool: stored !== default
      return `${expr} !== ${defaultValue}`;
    case 'int8':
    case 'int16':
    case 'int32':
      // 有符号整数：先转无符号 XOR，再转回有符号
      return `(${expr} ^ ${defaultValue}) | 0`;
    case 'uint8':
    case 'uint16':
    case 'uint32':
      // 无符号整数：直接 XOR
      return `${expr} ^ ${defaultValue}`;
    case 'int64':
    case 'uint64':
      // bigint：直接 XOR
      return `${expr} ^ ${defaultValue}`;
    case 'float32':
      // float32: 需要位操作转换
      return `xorFloat32(${expr}, ${defaultValue})`;
    case 'float64':
      // float64: 需要位操作转换
      return `xorFloat64(${expr}, ${defaultValue})`;
    case 'enum':
      // enum 作为 uint16 处理
      return `${expr} ^ ${defaultValue}`;
    default:
      return expr;
  }
}

/**
 * 生成 XOR 编码表达式（用于 setter）
 * actual ^ default = stored
 */
function generateXorEncode(valueExpr: string, defaultValue: string, typeKind: TypeKind): string {
  switch (typeKind) {
    case 'bool':
      // bool: value !== default
      return `${valueExpr} !== ${defaultValue}`;
    case 'int8':
    case 'int16':
    case 'int32':
      // 有符号整数：先转无符号 XOR
      return `(${valueExpr} >>> 0) ^ ${defaultValue}`;
    case 'uint8':
    case 'uint16':
    case 'uint32':
      // 无符号整数：直接 XOR
      return `${valueExpr} ^ ${defaultValue}`;
    case 'int64':
    case 'uint64':
      // bigint：直接 XOR
      return `${valueExpr} ^ ${defaultValue}`;
    case 'float32':
      // float32: 需要位操作转换
      return `xorFloat32(${valueExpr}, ${defaultValue})`;
    case 'float64':
      // float64: 需要位操作转换
      return `xorFloat64(${valueExpr}, ${defaultValue})`;
    case 'enum':
      // enum 作为 uint16 处理
      return `${valueExpr} ^ ${defaultValue}`;
    default:
      return valueExpr;
  }
}

/**
 * 生成字段 getter
 */
function generateFieldGetter(field: FieldReader): string {
  const name = field.name;
  const type = field.slotType;
  const defaultValue = extractDefaultValue(field);
  
  if (!type) {
    return `get ${name}(): unknown { return undefined; }`;
  }
  
  // 如果有默认值，使用 XOR 解码
  if (defaultValue !== undefined && type.kind !== 'text' && type.kind !== 'data') {
    const readExpr = `this.reader.get${capitalize(type.kind)}(${getByteOffset(field, type.kind)})`;
    const decodedExpr = generateXorDecode(readExpr, defaultValue, type.kind);
    return `get ${name}(): ${getTypeScriptTypeForSetter(type)} { return ${decodedExpr}; }`;
  }
  
  switch (type.kind) {
    case 'void':
      return `get ${name}(): void { return undefined; }`;
    case 'bool':
      return `get ${name}(): boolean { return this.reader.getBool(${field.slotOffset * 8}); }`;
    case 'int8':
      return `get ${name}(): number { return this.reader.getInt8(${field.slotOffset}); }`;
    case 'int16':
      return `get ${name}(): number { return this.reader.getInt16(${field.slotOffset * 2}); }`;
    case 'int32':
      return `get ${name}(): number { return this.reader.getInt32(${field.slotOffset * 4}); }`;
    case 'int64':
      return `get ${name}(): bigint { return this.reader.getInt64(${field.slotOffset * 8}); }`;
    case 'uint8':
      return `get ${name}(): number { return this.reader.getUint8(${field.slotOffset}); }`;
    case 'uint16':
      return `get ${name}(): number { return this.reader.getUint16(${field.slotOffset * 2}); }`;
    case 'uint32':
      return `get ${name}(): number { return this.reader.getUint32(${field.slotOffset * 4}); }`;
    case 'uint64':
      return `get ${name}(): bigint { return this.reader.getUint64(${field.slotOffset * 8}); }`;
    case 'float32':
      return `get ${name}(): number { return this.reader.getFloat32(${field.slotOffset * 4}); }`;
    case 'float64':
      return `get ${name}(): number { return this.reader.getFloat64(${field.slotOffset * 8}); }`;
    case 'text':
      return `get ${name}(): string { return this.reader.getText(${field.slotOffset}); }`;
    case 'data':
      return `get ${name}(): Uint8Array { return this.reader.getData(${field.slotOffset}); }`;
    default:
      return `get ${name}(): unknown { return undefined; }`;
  }
}

/**
 * 获取字节偏移量
 */
function getByteOffset(field: FieldReader, typeKind: TypeKind): number {
  switch (typeKind) {
    case 'bool':
      return field.slotOffset * 8; // bit offset
    case 'int8':
    case 'uint8':
      return field.slotOffset;
    case 'int16':
    case 'uint16':
    case 'enum':
      return field.slotOffset * 2;
    case 'int32':
    case 'uint32':
    case 'float32':
      return field.slotOffset * 4;
    case 'int64':
    case 'uint64':
    case 'float64':
      return field.slotOffset * 8;
    default:
      return field.slotOffset;
  }
}

/**
 * 生成字段 setter
 */
function generateFieldSetter(field: FieldReader): string {
  const name = field.name;
  const type = field.slotType;
  const paramType = getTypeScriptTypeForSetter(type);
  const defaultValue = extractDefaultValue(field);
  
  if (!type) {
    return `set${capitalize(name)}(value: unknown): void { /* TODO */ }`;
  }
  
  // 如果有默认值，使用 XOR 编码
  if (defaultValue !== undefined && type.kind !== 'text' && type.kind !== 'data') {
    const encodedExpr = generateXorEncode('value', defaultValue, type.kind);
    const setterMethod = `set${capitalize(type.kind)}`;
    return `set${capitalize(name)}(value: ${paramType}): void { this.builder.${setterMethod}(${getByteOffset(field, type.kind)}, ${encodedExpr}); }`;
  }
  
  switch (type.kind) {
    case 'void':
      return `set${capitalize(name)}(value: void): void { /* void */ }`;
    case 'bool':
      return `set${capitalize(name)}(value: ${paramType}): void { this.builder.setBool(${field.slotOffset * 8}, value); }`;
    case 'int8':
      return `set${capitalize(name)}(value: ${paramType}): void { this.builder.setInt8(${field.slotOffset}, value); }`;
    case 'int16':
      return `set${capitalize(name)}(value: ${paramType}): void { this.builder.setInt16(${field.slotOffset * 2}, value); }`;
    case 'int32':
      return `set${capitalize(name)}(value: ${paramType}): void { this.builder.setInt32(${field.slotOffset * 4}, value); }`;
    case 'int64':
      return `set${capitalize(name)}(value: ${paramType}): void { this.builder.setInt64(${field.slotOffset * 8}, value); }`;
    case 'uint8':
      return `set${capitalize(name)}(value: ${paramType}): void { this.builder.setUint8(${field.slotOffset}, value); }`;
    case 'uint16':
      return `set${capitalize(name)}(value: ${paramType}): void { this.builder.setUint16(${field.slotOffset * 2}, value); }`;
    case 'uint32':
      return `set${capitalize(name)}(value: ${paramType}): void { this.builder.setUint32(${field.slotOffset * 4}, value); }`;
    case 'uint64':
      return `set${capitalize(name)}(value: ${paramType}): void { this.builder.setUint64(${field.slotOffset * 8}, value); }`;
    case 'float32':
      return `set${capitalize(name)}(value: ${paramType}): void { this.builder.setFloat32(${field.slotOffset * 4}, value); }`;
    case 'float64':
      return `set${capitalize(name)}(value: ${paramType}): void { this.builder.setFloat64(${field.slotOffset * 8}, value); }`;
    case 'text':
      return `set${capitalize(name)}(value: ${paramType}): void { this.builder.setText(${field.slotOffset}, value); }`;
    case 'data':
      return `set${capitalize(name)}(value: ${paramType}): void { this.builder.setData(${field.slotOffset}, value); }`;
    default:
      return `set${capitalize(name)}(value: ${paramType}): void { /* TODO */ }`;
  }
}

/**
 * 获取 TypeScript setter 参数类型
 */
function getTypeScriptTypeForSetter(type: TypeReader | null): string {
  if (!type) return 'unknown';
  
  switch (type.kind) {
    case 'void': return 'void';
    case 'bool': return 'boolean';
    case 'int8':
    case 'int16':
    case 'int32':
    case 'uint8':
    case 'uint16':
    case 'uint32':
    case 'float32':
    case 'float64':
      return 'number';
    case 'int64':
    case 'uint64':
      return 'bigint';
    case 'text': return 'string';
    case 'data': return 'Uint8Array';
    default:
      return 'unknown';
  }
}

/**
 * 首字母大写
 */
function capitalize(str: string): string {
  return str.charAt(0).toUpperCase() + str.slice(1);
}

/**
 * 生成 Enum 的 TypeScript 代码
 */
function generateEnum(node: NodeReader): string {
  const lines: string[] = [];
  const enumName = getShortName(node.displayName);
  
  lines.push(`export enum ${enumName} {`);
  for (const enumerant of node.enumEnumerants) {
    lines.push(`  ${enumerant.name} = ${enumerant.codeOrder},`);
  }
  lines.push('}');
  
  return lines.join('\n');
}

/**
 * 获取 TypeScript 类型字符串
 */
function getTypeScriptType(type: TypeReader | null, allNodes: NodeReader[]): string {
  if (!type) return 'unknown';
  
  switch (type.kind) {
    case 'void': return 'void';
    case 'bool': return 'boolean';
    case 'int8':
    case 'int16':
    case 'int32':
    case 'uint8':
    case 'uint16':
    case 'uint32':
    case 'float32':
    case 'float64':
      return 'number';
    case 'int64':
    case 'uint64':
      return 'bigint';
    case 'text': return 'string';
    case 'data': return 'Uint8Array';
    case 'list': {
      const elementType = type.listElementType;
      if (!elementType) return 'unknown[]';
      const elemTsType = getTypeScriptType(elementType, allNodes);
      return `${elemTsType}[]`;
    }
    case 'struct': {
      const structNode = allNodes.find(n => n.id === type.typeId);
      if (structNode) {
        return getShortName(structNode.displayName);
      }
      return 'unknown';
    }
    case 'enum': {
      const enumNode = allNodes.find(n => n.id === type.typeId);
      if (enumNode) {
        return getShortName(enumNode.displayName);
      }
      return 'unknown';
    }
    default:
      return 'unknown';
  }
}

/**
 * 从 displayName 获取短名称
 * 例如 "test-schema.capnp:Person" -> "Person"
 */
function getShortName(displayName: string): string {
  const colonIndex = displayName.lastIndexOf(':');
  if (colonIndex >= 0) {
    return displayName.substring(colonIndex + 1);
  }
  // 如果没有冒号，可能是文件名，返回空或文件名
  return displayName.replace(/\.capnp$/, '').replace(/[^a-zA-Z0-9_]/g, '_');
}
